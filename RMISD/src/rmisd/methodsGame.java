package rmisd;

import java.util.ArrayList;

/**
 *
 * @author Navy
 */
public class methodsGame {
    public ArrayList RNGArrayPlayer (int a, ArrayList inputByPlayer){
        inputByPlayer.add(a);
        return inputByPlayer;
    }
    
    public boolean RNGArrayCheck (ArrayList inputByPlayer, ArrayList correctSequence){
        boolean result = false;
        for (int i = 0; i < inputByPlayer.size(); i++) {
            if (inputByPlayer.get(i)==correctSequence.get(i)) {
                result = true;
                System.out.println("Number "+i+" is correct.");
            }
            else{
                System.out.println("Number "+i+" is incorrect.");
                System.out.println("Game Over");
                result = false;
                break;
            }
        }
        return result;
    }
}



